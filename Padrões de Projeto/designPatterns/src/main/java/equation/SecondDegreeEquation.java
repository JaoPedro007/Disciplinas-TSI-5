package equation;

public class SecondDegreeEquation {
	private float a;
	private float b;
	private float c;
	
	public SecondDegreeEquation(float a, float b, float c) {
			this.a=a;
			this.b=b;
			this.c=c;
			
	}
	public final float getA() {
		return a;
	}
	
	public final float getB() {
		return b;
	}
	
	public final float getC() {
		return c;
	}
}
